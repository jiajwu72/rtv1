#include "rtv1.h"

void			add_obj(t_obj **alst, t_obj *new)
{
	if (!(*alst) || (*alst)->data == NULL)
	{
		*alst = new;
	}
	else
	{
		new->next = *alst;
		*alst = new;
	}
}
